public class RhombusPattern {
    public static void main(String[] args) {
        int n=4;
        int m=5;
        for( int row=1;row<=m;row++){
           
            for(int col=1;col<=n;col++){
                System.out.print("*");
            }
            System.out.println(" ");
            System.out.print(" ");
            
            

        }
        
    }
}
